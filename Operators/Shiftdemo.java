package Operators;

public class Shiftdemo {

    public static void main(String[] args) {
        int a = 7;
        System.out.println("Left Shift");
        System.out.println(a << 4); // 0111    01110000
        // 0001 << 0010


        System.out.println("Right Shift");
        System.out.println(a >> 3); //0111       0000 

    }

}
